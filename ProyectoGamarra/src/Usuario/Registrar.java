    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if("".equals(txtapellido.getText())|| "".equals(txtconfi.getText())|| "".equals(txtdni.getText())|| "".equals(txtnombre.getText())|| "".equals(txtpass.getText())|| "".equals(txtusua.getText())){
                
            }else{
                String sql1="SELECT * FROM cuentas where dni='"+txtdni.getText()+"';";
                so.Base="usuarios";
                so.conectar();
                rs1=so.verDatos(sql1);
                if(rs1.next()){//verificacion del dni
                    JOptionPane.showMessageDialog(null, "la persona ya esta registrada(dni en uso)");
                    txtdni.setText("");
                    so.desconectar();
                }
                else{//almacenamiento y corroborar la coincidencia de contrase√±as
                    so.conectar();
                    so.Base="usuarios";
                    String sql="SELECT * FROM cuentas where usuario='"+txtusua.getText()+"';";
                    rs=so.verDatos(sql);
                    if(rs.next()){
                        JOptionPane.showMessageDialog(null,"El usuario ya existe");
                        txtusua.setText("");
                    }
                
                    else{
                        if(txtpass.getText().equals(txtconfi.getText())){
                            registrar();
                        }else{
                            JOptionPane.showMessageDialog(null,"las contrase√±as no coinciden");
                            txtconfi.setText("");
                        }
                    }   
                }
                
            }
            
        } catch (Exception ex){
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    public void registrar() throws SQLException{
        so.Base="usuarios";
        String sql="insert cuentas values('"+txtusua.getText()+"','"+txtpass.getText()+"','"+txtnombre.getText()+"','"+txtapellido.getText()+"','"+txtdni.getText()+"','"+cmbedad.getSelectedItem().toString()+"')";
        so.conectar();
        so.actualizar(sql);
        so.desconectar();
        JOptionPane.showMessageDialog(null, "Registrado correctamente");
        dispose();
        vaciarCajas();
    }

