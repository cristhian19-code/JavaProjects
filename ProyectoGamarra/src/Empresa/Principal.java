    private void openMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMenuItemActionPerformed
        try {
            ov.Base="administracion";
            ov.conectar();
            rs=ov.verDatos("SELECT * FROM admin where habilitado='0'");
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "ya se inicio sesion");
            }else{
                JOptionPane.showMessageDialog(null,"no se iniciado sesion");
                Inicio_sesion so = new Inicio_sesion();
                so.setLocationRelativeTo(null);
                so.setVisible(true);
            }
        } catch (HeadlessException | SQLException ex) {
        }
    }//GEN-LAST:event_openMenuItemActionPerformed

    private void saveMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveMenuItemActionPerformed
        try{
            ov.conectar();
            ov.Base="administracion";
            rs=ov.verDatos("Select * from admin where habilitado = '0';");
            if(rs.next()){
                Almacenar alm = new Almacenar();
                alm.setLocationRelativeTo(null);
                alm.setVisible(true);       
            }else{
               res=JOptionPane.showConfirmDialog(null,"Desea Iniciar Sesion??","No Iniciaste Sesion",JOptionPane.YES_NO_OPTION);
                if(res==0){
                    Inicio_sesion ini = new Inicio_sesion();
                    ini.setLocationRelativeTo(null);
                    ini.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null,"Aun no se Inicio Sesion");
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveMenuItemActionPerformed

